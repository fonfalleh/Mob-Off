public class Coordinates {

    private double xcoord;
    private double ycoord;

    public Coordinates(double x, double y){
        xcoord = x;
        ycoord = y;
    }

    public double getX() {
        return xcoord;
    }

    public double getY() {
        return ycoord;
    }


}
